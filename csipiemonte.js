function processDataMultiIndex(response, result) {

    var indexes = result.replace('\n','').split(';');
    var length = parseFloat(indexes[0]);
    var cols = indexes.length - 2;

    var arrayBuffer = response;
    //var str = JSON.stringify(Array.from(new Uint32Array(arrayBuffer)));
    if (arrayBuffer) {
        var minpos, maxpos, minrap, maxrap;
        var dates = new Array();

        var intArray1 = new Uint32Array(arrayBuffer, 0, (length-1));
        var floatArray = new Float32Array(arrayBuffer, (length-1)*4, (length-1)*cols);

        var valueCounter = 0;
        for (var i = 0; i < intArray1.length; i++) {
            var datacom = {};
            var datapos = new Array();
            var datarap = new Array();

            datacom.comune_ist = fill(intArray1[i].toString(), 6);
            //datacom.pop_tot = intArray2[i];
            for (var j=0; j<cols; j++) {
                var prop = indexes[j+2];
                var value = floatArray[valueCounter];

                if (prop.length==4) {
                    if (!layersClassificationStartString || prop>layersClassificationStartString) {
                        datapos.push({giorno: prop, value: value});
                        if (!minpos && minpos!=0) {
                            minpos = value;
                        }
                        if (!maxpos && maxpos!=0) {
                            maxpos = value;
                        }
                        minpos = Math.min(minpos, value);
                        maxpos = Math.max(maxpos, value);
                        if (!dates.includes(prop)) {
                            dates.push(prop);
                        }
                    }
                } else if (prop.length==5 && prop.charAt(0)=='r') {
                    var entry = prop.substring(1);
                    if (!layersClassificationStartString || entry>layersClassificationStartString) {
                        datarap.push({giorno: entry, value: value});
                        if (!minrap && minrap!=0) {
                            minrap = value;
                        }
                        if (!maxrap && maxrap!=0) {
                            maxrap = value;
                        }
                        minrap = Math.min(minrap, value);
                        maxrap = Math.max(maxrap, value);
                        if (!dates.includes(entry)) {
                            dates.push(entry);
                        }
                    }
                }

                valueCounter++;
            }
            datacom.POSITIVI = datapos;
            datacom.RAPPORTO = datarap;
            classificationData[datacom.comune_ist] = datacom;
            let nome_comune = codici_istat[datacom.comune_ist];
            datacom.comune = nome_comune;
            finalData.push(datacom);
        }
    }

    layersClassificationRange['POSITIVI'].min = minpos;
    layersClassificationRange['POSITIVI'].max = maxpos;
    layersClassificationRange['RAPPORTO'].min = minrap;
    layersClassificationRange['RAPPORTO'].max = maxrap;
    dates.sort(function(a, b){return a.x - b.x});
    layersClassificationDates = dates;


    var labels = new Array();
    var data = new Array();
    for (var i=0; i<layersClassificationDates.length; i++) {
        labels.push(layersClassificationDates[i].substring(2,4)+'/'+layersClassificationDates[i].substring(0,2));
        data.push({x: new Date(layersClassificationYear, layersClassificationDates[i].substring(0,2), layersClassificationDates[i].substring(2,4)), y: 0});
    }

    return finalData;

}

function fill(str, count) {
    for (var i=str.length; i<count; i++) {
        str = '0' + str;
    }
    return str;
}


